    private void cboTipoMaterialItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboTipoMaterialItemStateChanged
        // TODO add your handling code here:
        //**Llenamos el combobox de Subtipo de Material, dependiendo de la opción seleccionada en el primer combobox
        String opcion =(String)cboTipoMaterial.getSelectedItem();
        if(opcion.equals("Material Escrito"))
        {cboSubtipoMaterial.setModel(new javax.swing.DefaultComboBoxModel<>(new String[]{"Libro", "Revista"}));

        }if(opcion.equals("Material Audiovisual"))
        {cboSubtipoMaterial.setModel(new javax.swing.DefaultComboBoxModel<>(new String[]{"CD","DVD"}));}
    }//GEN-LAST:event_cboTipoMaterialItemStateChanged

