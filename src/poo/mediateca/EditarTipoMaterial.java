    private void btnGuardarTipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarTipoActionPerformed
        // TODO add your handling code here:
        /**Abrir el formulario correspondiente según la opción seleccionada en el combobox de Subtipo material **/
        String item = cboSubtipoMaterial.getSelectedItem().toString();
        if(item.equals("Libro"))
        {
            dispose();
            EditarLibro elib = new EditarLibro();
            elib.setVisible(true);
        }
        if(item.equals("Revista"))
        {
            dispose();
            EditarRevista erev = new EditarRevista();
            erev.setVisible(true);
        }
        if(item.equals("CD"))
        {
            dispose();
            EditarCD ecd = new EditarCD();
            ecd.setVisible(true);
        }
        if(item.equals("DVD"))
        {
            dispose();
            EditarDVD edvd = new EditarDVD();
            edvd.setVisible(true);
        }

    }//GEN-LAST:event_btnGuardarTipoActionPerformed

